    private void Borrar_tabla() {
        DefaultTableModel temp = (DefaultTableModel) this.jTabla_info.getModel();
        int fila = this.jTabla_info.getRowCount();

        try {
            for (int i = fila; i > 0; i--) {
                temp.removeRow(0);
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            men.showMessageDialog(null, "Error a eliminar fila en la tabla", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

