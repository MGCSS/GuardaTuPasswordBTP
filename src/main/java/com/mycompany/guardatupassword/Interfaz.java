    private void Boton_SesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_SesionActionPerformed

        this.reset_campos();

        if (this.isSesion == false) {
            if (this.gestorUsuario.comprobarUsuario() == true) { //Si existe algun usuario
                this.jTextField_usuario.setText(this.usuario.getNombreUsuario());
                //this.jPassword_sesion.setText(this.usuario.getPassword());
                this.jBoton_registro.setEnabled(false);
                this.jBoton_aceptar_sesion.setEnabled(true);
            } else {
                this.jBoton_aceptar_sesion.setEnabled(false);
                this.jBoton_registro.setEnabled(true);
            }

            this.dispose();
            this.Boton_Sesion.setText("Cerrar Sesión");
            this.jFrame_sesion.setLocationRelativeTo(this);
            this.jFrame_sesion.setVisible(true);
            this.isSesion = true;
        } else {
            this.Boton_Sesion.setText("Iniciar Sesión");
            this.jLabel_usuario.setText("Sesión no iniciada");
            this.jPanel_nuevaClaves.setVisible(false);
            this.jPanel_listado.setVisible(false);
            this.jPanel_modifica_pass.setVisible(false);
            this.isSesion = false;
        }


    }//GEN-LAST:event_Boton_SesionActionPerformed

